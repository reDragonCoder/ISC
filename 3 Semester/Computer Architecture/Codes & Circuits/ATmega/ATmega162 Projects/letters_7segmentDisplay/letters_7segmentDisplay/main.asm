.INCLUDE <M162DEF.INC>

; STACK POINTER CONFIGURATION
	LDI R16, 0X04
	OUT SPH, R16	; THIS IS THE UPPER PART OF THE SP REGISTER
	LDI R16, 0XFF
	OUT SPL, R16	; THIS IS THE LOWER PART OF THE SP REGISTER

; WE DECLARE THE POINTER "A" AS OUTPUT
	LDI R16, 0XFF
	OUT DDRA, R16	; WE CONFIGURE THE PORT AS OUTPUT

; WE DEFINE THE DATA (LETTERS) FOR THE REGISTERS
CYCLE1:
	LDI R17, 0x77 ; A
	LDI R18, 0x7C ; b
	LDI R19, 0x39 ; C
	LDI R20, 0x5E ; d
	LDI R21, 0x79 ; E
	LDI R22, 0x71 ; F
	LDI R23, 0x6F ; g
	LDI R24, 0x76 ; H
	LDI R25, 0x30 ; I
	LDI R26, 0x1E ; J
	LDI R27, 0x38 ; L
	LDI R28, 0x54 ; n

	OUT PORTA, R17
	RCALL SUBROUTINE
	OUT PORTA, R18
	RCALL SUBROUTINE
	OUT PORTA, R19
	RCALL SUBROUTINE
	OUT PORTA, R20
	RCALL SUBROUTINE
	OUT PORTA, R21
	RCALL SUBROUTINE
	OUT PORTA, R22
	RCALL SUBROUTINE
	OUT PORTA, R23
	RCALL SUBROUTINE
	OUT PORTA, R24
	RCALL SUBROUTINE
	OUT PORTA, R25
	RCALL SUBROUTINE
	OUT PORTA, R26
	RCALL SUBROUTINE
	OUT PORTA, R27
	RCALL SUBROUTINE
	OUT PORTA, R28
	RCALL SUBROUTINE
	RJMP CYCLE2

CYCLE2:
	LDI R17, 0x3F ; O
	LDI R18, 0x73 ; p
	LDI R19, 0x67 ; q
	LDI R20, 0x50 ; r
	LDI R21, 0x6D ; S
	LDI R22, 0x78 ; t
	LDI R23, 0x3E ; U
	LDI R24, 0x1C ; v
	LDI R25, 0x6E ; y
	LDI R26, 0x5B ; Z

	OUT PORTA, R17
	RCALL SUBROUTINE
	OUT PORTA, R18
	RCALL SUBROUTINE
	OUT PORTA, R19
	RCALL SUBROUTINE
	OUT PORTA, R20
	RCALL SUBROUTINE
	OUT PORTA, R21
	RCALL SUBROUTINE
	OUT PORTA, R22
	RCALL SUBROUTINE
	OUT PORTA, R23
	RCALL SUBROUTINE
	OUT PORTA, R24
	RCALL SUBROUTINE
	OUT PORTA, R25
	RCALL SUBROUTINE
	OUT PORTA, R26
	RCALL SUBROUTINE
	RJMP CYCLE1

SUBROUTINE:	
			LDI R29, 8
			LDI R30, 150
			LDI R31, 128
			
L1:			
			DEC R31
			BRNE L1
			DEC R30
			BRNE L1
			DEC R29
			BRNE L1

			RET



			

